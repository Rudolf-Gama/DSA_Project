// It will find the minimum distance
int mindistance(int distance[], int stat[])
{
    int minimum = INT_MAX, ind;
    for (int k = 0; k < 56; k++)
    {
        if (stat[k] == 0 && distance[k] <= minimum)
        {
            minimum = distance[k];
            ind = k;
        }
    }
    return ind;
}

