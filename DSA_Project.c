// single source - single destination
void single_destin(int v, int graph[v][v], int src, int destination, char station[56][100])
{
    int cost[v][v], distance[v], visited[v], path[v];
    int nextnode;
    int count;

    for (int i = 0; i < v; i++)
    {
        for (int j = 0; j < v; j++)
        {

            if (graph[i][j] == 0)
            {
                cost[i][j] = INFINITY;
            }
            else
            {
                cost[i][j] = graph[i][j];
            }
        }
    }
    int mindistance;

    // creating distance visited path
    for (int i = 0; i < v; i++)
    {
        distance[i] = cost[src][i];
        path[i] = src;
        visited[i] = 0;
    }

    distance[src] = 0;
    visited[src] = 1;
    count = 1;

    while (count < v - 1)
    {
        mindistance = INFINITY;
        for (int i = 0; i < v; i++)
        {
            if (distance[i] < mindistance && !visited[i])
            {
                mindistance = distance[i];
                nextnode = i;
            }
        }
        visited[nextnode] = 1;
        // we have to extract all neigh
        for (int i = 0; i < v; i++)
        {
            if (mindistance + cost[nextnode][i] < distance[i] && !visited[i])
            {
                distance[i] = mindistance + cost[nextnode][i];
                path[i] = nextnode;
            }
        }
        count++;
    }

    printf("\n The distance from %s to %s: %d\n", station[src], station[destination], distance[destination]);
    return;
}

